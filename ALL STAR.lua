function allstargetfrist()
    local pathvalue = 0 
    local VarEn = nil
    for i,v in pairs(game:GetService("Workspace").Enemies:GetChildren()) do
        if v:FindFirstChild('HumanoidRootPart') and v:FindFirstChild('PathNumber') then
            local Varpath = v.PathNumber.Value 
            if tonumber(Varpath) > pathvalue then
                VarEn = v 
                pathvalue = Varpath
            end
        end
    end
    return VarEn
end
function gethill(Position)
    local hillframe = math.huge
    local VarEn = nil
    for i,v in pairs(game:GetService("Workspace").FolderPartSkyhub:GetChildren()) do
        if v:IsA('Part') then
            local Varpath = (Position - v.Position).Magnitude
            if tonumber(Varpath) < hillframe then
                VarEn = v 
                hillframe = Varpath
            end
        end
    end
    return VarEn
end
